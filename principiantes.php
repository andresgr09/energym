                            <p><strong>Sentadillas</strong> Las sentadillas son uno de los ejercicios más efectivos para trabajar los músculos de las piernas y glúteos. Colócate en posición de pie con los pies separados a la anchura de los hombros, baja las caderas hacia atrás y hacia abajo como si te fueras a sentar en una silla imaginaria. Asegúrate de que las rodillas no sobrepasen los dedos de los pies y levántate de nuevo.</p>

<p><strong>Press de banca:</strong> Este ejercicio trabaja principalmente los músculos del pecho, pero también involucra los tríceps y los hombros. Acuéstate boca arriba en un banco, agarra la barra con las manos separadas a la anchura de los hombros y bájala lentamente hasta que toque tu pecho, luego levántala de nuevo.</p>

<p><strong>Dominadas:</strong> Las dominadas son un ejercicio de cuerpo completo que se enfoca principalmente en los músculos de la espalda, los brazos y los hombros. Agarra una barra con las manos separadas a la anchura de los hombros y levanta tu cuerpo hacia la barra, manteniendo los codos doblados y los hombros hacia abajo.</p>

<p><strong>Peso muerto:</strong> Este ejercicio trabaja principalmente los músculos de las piernas, pero también involucra los músculos de la espalda y los glúteos. Coloca una barra con peso en el suelo, párate detrás de ella con los pies separados a la anchura de los hombros, inclínate hacia adelante y agarra la barra con las manos separadas a la anchura de los hombros. Levántate con la barra manteniendo la espalda recta y los hombros hacia abajo.</p>

<p><strong>Curl de bíceps con mancuernas:</strong> Este ejercicio trabaja los músculos del bíceps. Sostén una mancuerna en cada mano, párate con los pies separados a la anchura de los hombros y levanta las mancuernas hacia tus hombros, manteniendo los codos pegados al cuerpo. Baja las mancuernas lentamente y repite.</p>

